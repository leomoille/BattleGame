package controller;

import games.GameEvaluator;
import model.Deck;
import model.Player;
import model.PlayingCard;

import view.GameViewable;

import java.util.ArrayList;
import java.util.List;

public class GameController {
    enum GameState {
        AddingPlayers, CardsDealt, WinnerRevealed;
    }

    Deck deck;
    List<Player> players;
    Player winner;
    GameViewable view;

    GameState gameState;
    GameEvaluator gameEvaluator;

    public GameController(Deck deck, GameViewable view, GameEvaluator gameEvaluator) {
        super();
        this.deck = deck;
        this.view = view;
        this.players = new ArrayList<Player>();
        this.gameState = GameState.AddingPlayers;
        this.gameEvaluator = gameEvaluator;
        this.view.setController(this);
    }

    public void run() {
        while (this.gameState == GameState.AddingPlayers) {
            this.view.promptForPlayerName();
        }

        switch (this.gameState) {
            case CardsDealt -> this.view.promptForFlip();
            case WinnerRevealed -> this.view.promptForNewGame();
        }
    }

    public void addPlayer(String playerName) {
        if (this.gameState == GameState.AddingPlayers) {
            this.players.add(new Player(playerName));
            this.view.showPlayerName(players.size(), playerName);
        }
    }

    public void startGame() {
        if (this.gameState != GameState.CardsDealt) {
            this.deck.shuffle();
            int playerIndex = 1;
            for (Player player : this.players) {
                player.addCardToHand(deck.removeTopCard());
                this.view.showFaceDownCardForPlayer(playerIndex++, player.getName());
            }
            this.gameState = GameState.CardsDealt;
        }
        this.run();
    }

    public void flipCards() {
        int playerIndex = 1;
        for (Player player : players) {
            PlayingCard pc = player.getCard(0);
            pc.flip();
            view.showCardForPlayer(playerIndex++, player.getName(), pc.getRank().toString(), pc.getSuit().toString());
        }

        evaluateWinner();
        displayWinner();
        rebuildDeck();
        gameState = GameState.WinnerRevealed;
        this.run();
    }

    void evaluateWinner() {
        winner = gameEvaluator.evaluateWinner(players);
    }

    void displayWinner() {
        view.showWinner(winner.getName());
    }

    void rebuildDeck() {
        for (Player player : players) {
            deck.returnCardToDeck(player.removeCard());
        }
    }

    void exitGame() {
        System.exit(0);
    }

    public void nextAction(String nextChoice) {
        if ("q".equals(nextChoice)) {
            exitGame();
        } else {
            startGame();
        }
    }

}
